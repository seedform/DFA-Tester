package com.seedform.dfatester.viewer;

import android.os.Bundle;
import android.support.v4.app.FragmentTransaction;
import android.support.v4.view.ViewPager;
import android.support.v7.app.ActionBar;
import android.support.v7.app.ActionBar.Tab;
import android.support.v7.app.ActionBar.TabListener;
import android.view.MenuItem;

import com.seedform.dfatester.R;
import com.seedform.dfatester.app.BaseActivity;
import com.seedform.dfatester.app.SectionsPagerAdapter;
import com.seedform.dfatester.util.Data;
import com.seedform.dfatester.util.Tool;

public class DFAViewerActivity extends BaseActivity implements TabListener {

    private SectionsPagerAdapter mSectionsPagerAdapter;
    private ViewPager mViewPager;

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_dfa);
        setUpUI();
    }

    @SuppressWarnings("unchecked")
    private void setUpUI() {
        getSupportActionBar().setTitle("DFA " + String.valueOf(Data.getSelectedDFA().getId()));

        mSectionsPagerAdapter = new SectionsPagerAdapter(getSupportFragmentManager(),
                getResources().getStringArray(R.array.tab_titles),
                new Class[] {
                        AlphabetListFragment.class, StateListFragment.class,
                        StringTestFragment.class
                });

        final ActionBar actionBar = getSupportActionBar();
        actionBar.setNavigationMode(ActionBar.NAVIGATION_MODE_TABS);
        actionBar.setDisplayHomeAsUpEnabled(true);
        actionBar.setHomeButtonEnabled(true);

        mViewPager = (ViewPager) findViewById(R.id.pager);
        mViewPager.setAdapter(mSectionsPagerAdapter);
        mViewPager.setOnPageChangeListener(new ViewPager.SimpleOnPageChangeListener() {
            @Override
            public void onPageSelected(int position) {
                actionBar.setSelectedNavigationItem(position);
            }
        });

        // add tabs for each fragment to the action bar.
        for (int i = 0; i < mSectionsPagerAdapter.getCount(); i++) {
            actionBar.addTab(actionBar.newTab().setText(mSectionsPagerAdapter.getPageTitle(i))
                    .setTabListener(this));
        }
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        switch (item.getItemId()) {
            case android.R.id.home:
                finish();
                return true;
            default:
                return super.onOptionsItemSelected(item);
        }
    }

    @Override
    public void onTabReselected(Tab tab, FragmentTransaction ft) {
    }

    @Override
    public void onTabUnselected(Tab tab, FragmentTransaction ft) {
    }

    @Override
    public void onTabSelected(Tab tab, FragmentTransaction ft) {
        Tool.hideSoftKeyboard(this);
        mViewPager.setCurrentItem(tab.getPosition());
    }

}
